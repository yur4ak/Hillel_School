package lesson3;

public class SimpleCalc {

    public static int add(int x, int y) {
        int result = x+y;
        return result;

    }

    public static int subs(int x, int y) {
        int result = x-y;
        return result;
    }

    public static int mult(int x, int y) {
        int result = x*y;
        return result;
    }

    public static int div(int x, int y) {
        int result = x/y;
        return result;
    }
}
